CREATE TABLE EMPLOYEE (
    EMP_ID INT NOT NULL PRIMARY KEY IDENTITY(1,1),
    EMP_FNAME VARCHAR(20) NOT NULL,
    EMP_LNAME VARCHAR(20) NOT NULL,
    EMP_STREET VARCHAR(20) NOT NULL,
    EMP_CITY VARCHAR(20) NOT NULL,
    EMP_PROVINCE VARCHAR(20) NOT NULL,
    EMP_ZIP VARCHAR(20) NOT NULL,
    EMP_DOB DATE NOT NULL,
    EMP_HIRE_DATE DATE NOT NULL,
    EMP_FIRE_DATE DATE,
    EMP_DEPART_DATE DATE,
    EMP_LOCKER INT,
    EMP_PAY_RATE DECIMAL(8,2) NOT NULL,
    EMP_VACATION_ENTITLEMENT NUMERIC(5,0) NOT NULL,
    EMP_SICK_DAYS_ENTITLEMENT NUMERIC(5,0) NOT NULL,
    DEPT_ID INT NOT NULL,
    ROLE_CODE INT NOT NULL,
    FOREIGN KEY (DEPT_ID) REFERENCES DEPARTMENT(DEPT_ID),
    FOREIGN KEY (ROLE_CODE) REFERENCES EMPLOYEE_ROLE(ROLE_CODE)
)